package com.java.core.proceduralprogramming;

public class DataTypesDemo {
    public static void main(String[] args) {
        // age - number
        // name - Strings
        // height - 5.11
        // <DATA_TYPE> <VARIABLE_NAME> = <VALUE>;
        int age = 32; // primitive data types
        String name = "Mohit"; // non-primitive data types
        char gender = 'M';
        byte b = 'a';
        long distance = 321321321323231L;
        float height = 5.11f;
        double doubleHeight = 6; // 6.2 both float and integer type accepted
        boolean isTrue = true;
    }
}
