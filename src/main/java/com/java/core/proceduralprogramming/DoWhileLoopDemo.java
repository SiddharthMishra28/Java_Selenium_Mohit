package com.java.core.proceduralprogramming;

public class DoWhileLoopDemo {
    public static void main(String[] args) {
        // do {
        // ....
        // ....
        // ....
        // } while(<CONDITION>);
        int i=0;
        do { // EXECUTES ATLEAST ONCE
            System.out.println("Iterating for "+i);
            i++;
        } while(i<=-1);
    }
}
